    private void colores(){
        final StyleContext cont = StyleContext.getDefaultStyleContext();
        //Colores
        final AttributeSet attblue = cont.addAttribute(cont.getEmptySet(), StyleConstants.Foreground, new Color(27, 35, 142));
        final AttributeSet attred = cont.addAttribute(cont.getEmptySet(), StyleConstants.Foreground, new Color(142, 27, 27 ));
        final AttributeSet attgreen = cont.addAttribute(cont.getEmptySet(), StyleConstants.Foreground, new Color(70, 142, 27 ));
        final AttributeSet attblack = cont.addAttribute(cont.getEmptySet(), StyleConstants.Foreground, new Color(0,0,0));
        final AttributeSet attpurple = cont.addAttribute(cont.getEmptySet(), StyleConstants.Foreground, new Color(109, 27, 142 ));
        //ESTILO
        DefaultStyledDocument doc = new DefaultStyledDocument(){
            public void insertString(int offset, String str, AttributeSet a) throws BadLocationException{
                super.insertString(offset, str, a);
                
                String texto = getText(0, getLength());
                int before = findLastNonWordChar(texto, offset);
                if (before < 0) {
                    before = 0;
                }
                int after = findFirstNonWordChar(texto, offset+str.length());
                int wordL = before;
                int wordR = before;
                while(wordR <= after){
                    if (wordR == after || String.valueOf(texto.charAt(wordR)).matches("\\W")) {
                        //PALABRAS RESERBADAS
                        if (texto.substring(wordL, wordR).matches("(\\W)*(program|if|else|fi|do|until|while|read|write|float|int|bool|not|and|or)")) {
                            setCharacterAttributes(wordL, wordR - wordL, attgreen, false);
                        }else{
                            setCharacterAttributes(wordL, wordR - wordL, attblack, false);
                        }
                        wordL = wordR;
                    }
                    wordR++;
                }
            }
            public void remove(int offset, int len) throws BadLocationException{
                super.remove(offset, len);
                String text = getText(0, getLength());
                int before = findLastNonWordChar(text, offset);
                if (before < 0) {
                    before = 0;
                }
            }
        };
        
        JTextPane txt = new JTextPane(doc);
        String temp = jtpCode.getText();
        jtpCode.setStyledDocument(txt.getStyledDocument());
        jtpCode.setText(temp);
    }
