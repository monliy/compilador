    //METODO PARA ENCONTRAR LAS ULTIMAS CADENAS
    private int findLastNonWordChar(String texto, int index){
        while(--index >= 0){
            //      \\W = [A-Za-z0-9]
            if (String.valueOf(texto.charAt(index)).matches("\\W")) {
                break;
            }   
        }
        return index;
    }
    
    //METODO PARA ENCONTRAR LAS PRIMERA CADENAS
    private int findFirstNonWordChar(String texto, int index){
        while(index < texto.length()){
            //      \\W = [A-Za-z0-9]
            if (String.valueOf(texto.charAt(index)).matches("\\W")) {
                break;
            }
            index++;
        }
        return index;
    }
    
